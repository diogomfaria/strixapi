package br.com.fiap.strixapi.model;

public class User {
    
    //atributos
    private String nome;
    private int idade;

    //Getters and Setters
    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public int getIdade() {
        return idade;
    }
    public void setIdade(int idade) {
        this.idade = idade;
    } 
}
